# creating a list
country = ["Nepal", "India", "China"]
# appending a element to last of a list
country.append("Bhutan")
# removing an element from list
country.remove("China")
# removing a element from list by indexing
del(country[1])
# inserting a element to list by indexing
country.insert(1, "Maldives")

# creating a set
country1 = {"Nepal", "India", "China"}
# adding element to set
country1.add("Bhutan")
# removing element from set
country1.remove("Nepal")
