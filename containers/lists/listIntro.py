# List are always declared with [] big brackets which is very similar to array
# but can contain the unrelated data types collectively, too.

lst = []
print(lst)

lst = [10, 20, "Arjun", -10, -30.6]
print(lst)

# Indexing
print(lst[3])

# Slicing
print(lst[3:5])

# Repitition
print(lst*4)

# Length
print(len(lst))
