r = range(5) # 5 means the maximum no of elements i.e. index from 0 to 5.

for i in r:
    print(i)

print("Another Range : ")    
r = range(1,6) #First Parameter means the starting of range and second parameter means the end of the range.
for i in r:
    print(i)
    
print("Another Range : ")
r = range(1,20,3) #First Parameter means the starting of range, second parameter means the end of the range and third parameter means the step operation in range.
for i in r:
    print(i)